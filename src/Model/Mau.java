package Model;

/**
 *
 * @author tuanb
 */
public class Mau {
    private int id;
    private String tenMau;
    private int trangThai;

    public Mau() {
    }

    public Mau(int id, String tenMau, int trangThai) {
        this.id = id;
        this.tenMau = tenMau;
        this.trangThai = trangThai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenMau() {
        return tenMau;
    }

    public void setTenMau(String tenMau) {
        this.tenMau = tenMau;
    }

    public int getTrangThai() {
        return trangThai;
    }

    public void setTrangThai(int trangThai) {
        this.trangThai = trangThai;
    }

    
}
    