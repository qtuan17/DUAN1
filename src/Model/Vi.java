package Model;

/**
 *
 * @author tuanb
 */
public class Vi {
    private int id;
    private String tenVi;
    private int trangThai;

    public Vi() {
    }

    public Vi(int id, String tenVi, int trangThai) {
        this.id = id;
        this.tenVi = tenVi;
        this.trangThai = trangThai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenVi() {
        return tenVi;
    }

    public void setTenVi(String tenVi) {
        this.tenVi = tenVi;
    }

    public int getTrangThai() {
        return trangThai;
    }

    public void setTrangThai(int trangThai) {
        this.trangThai = trangThai;
    }
    
    
}

